import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const index = JSON.parse(fs.readFileSync(path.join(root, 'data/receipts-index.json'), 'utf8'));

const semanticMap = {
  'aime-reasoning': ['aime', 'math', 'reasoning', 'boxed', 'bunn', 'buun', '30k', 'ablation', 'abliterated', 'huihui', 'stock', 'qwen3.6'],
  'bunn-comparison': ['bunn', 'buun', 'ablation', 'abliterated', 'huihui', 'stock', 'bf16', '30k', 'qwen3.6', 'aime'],
  'kv-cache': ['kv', 'cache', 'ctk', 'ctv', 'q8_0', 'q4_0', 'f16', 'k cache', 'v cache', 'turboquant'],
  'context-capacity': ['context', 'long context', '128k', '262k', '524k', '786k', '1m', 'capacity', 'fits', 'wall'],
  'quality': ['quality', 'correctness', 'passkey', 'needle', 'retrieval', 'kld', 'perplexity', 'aime', 'answer'],
  'negative-evidence': ['negative', 'no delta', 'no-delta', 'slower', 'blocked', 'timeout', 'unsafe', 'failed', 'wall', 'mixed'],
  'runtime-speed': ['speed', 'throughput', 'tok/s', 'prefill', 'decode', 'flash', 'flash-attn', 'llama-bench'],
  'realrag': ['realrag', 'rag', 'entity-hop', 'path', 'rerank', 'verifier', 'evidence', 'retrieval'],
  'model-library': ['model card', 'llama', 'qwen', 'mistral', 'gemma', 'library'],
};

const conceptHints = Object.fromEntries(
  Object.entries(semanticMap).map(([concept, terms]) => [concept, terms.join(' ')])
);

const rows = index.receipts.map((r) => ({
  ...r,
  url: `https://github.com/sztlink/boring-receipts/blob/main/${r.file}`,
  localUrl: r.file.replace(/^receipts\//, 'https://github.com/sztlink/boring-receipts/blob/main/receipts/'),
}));

const out = `// Generated by scripts/build-docs-receipts-search.mjs. Do not edit by hand.\n` +
`window.BORING_RECEIPTS_INDEX = ${JSON.stringify({ updated: index.updated, receipts: rows, semanticMap, conceptHints }, null, 2)};\n`;

fs.writeFileSync(path.join(root, 'docs/receipts-search.js'), out);
console.log(`wrote docs/receipts-search.js (${rows.length} receipts)`);
